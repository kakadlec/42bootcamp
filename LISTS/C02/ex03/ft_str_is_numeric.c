int		ft_str_is_numeric(char *str)
{
	int i;
	int curr;

	i = 0;
	while (str[i] != '\0')
	{
		curr = str[i];
		if (!(curr >= '0' && curr <= '9'))
			return (0);
		i++;
	}
	return (1);
}
