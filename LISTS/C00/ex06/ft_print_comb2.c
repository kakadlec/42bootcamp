#include <unistd.h>

void	ft_print(char a, char b, char c, char d)
{
	write(1, &a, 1);
	write(1, &b, 1);
	write(1, " ", 1);
	write(1, &c, 1);
	write(1, &d, 1);
	if (a != '9' || b != '8' || c != '9' || d != '9')
		write(1, ", ", 2);
}

void	ft_print_comb2(void)
{
	char a[4];

	a[0] = '0';
	a[1] = '0';
	while (a[0] <= '9')
	{
		while (a[1] <= '9')
		{
			a[2] = a[0];
			a[3] = a[1] + 1;
			while (a[2] <= '9')
			{
				while (a[3] <= '9')
				{
					ft_print(a[0], a[1], a[2], a[3]);
					a[3]++;
				}
				a[3] = '0';
				a[2]++;
			}
			a[1]++;
		}
		a[1] = '0';
		a[0]++;
	}
}
